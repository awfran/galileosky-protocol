package main

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestCrc(t *testing.T) {
	checkBytes := []byte{0x01, 0x17, 0x80, 0x01, 0x82, 0x02, 0x10, 0x03, 0x38, 0x36, 0x32, 0x30, 0x35, 0x37, 0x30,
		0x34, 0x37, 0x37, 0x34, 0x35, 0x35, 0x33, 0x31, 0x04, 0x32, 0x00}

	assert.Equal(t, uint16(18613), crc16(checkBytes))
}
